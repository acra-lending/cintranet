                    <h4>Building Authentic Relationships – Part 6</h4>

                    <h5>Growing in a Relationship</h5>

                    <p>Many people go into relationships hoping to make a particular 
                    person more like themselves, to sway their opinions and to 
                    change their habits. All this does however, is create resentment, 
                    and blocks the relationship from reaching its fullest potential.</p>

                    <p>Believe me, I know.</p>

                    <p>The quickest way to a crappy relationship is to try to change the 
                    person whom you have a relationship with.
                    The only person you should want to change is yourself.</p>

                    <p>Relationships aren’t meant for forced change.</p>
                    
                    <p>They are meant to help us grow, but how we grow 
                    is up to the person facing the path of growth.</p>

                    <p>After dedicating myself to a path of personal 
                    development I unfortunately, developed the my-way-is-right-yours-is-not attitude. 
                    This hindered my ability to create empowering relationships. 
                    I went into relationships thinking that because it worked for 
                    me it had to work for everyone else. What I failed to do was 
                    appreciate the individuality of the people whom I had relationships with.</p>

                    <p>Don’t look to change anyone but yourself; otherwise you’re wasting your time. 
                    Learn to understand people’s uniqueness. Immerse yourself in different perspectives. 
                    It’s okay if you don’t always agree. Relationships would be stale and boring if that was the case.</p>

                    <p>Once you begin to appreciate the uniqueness of others you will soon begin 
                    to grow and even appreciate your own uniqueness more.</p>
